using System.Collections;
using System.Collections.Generic;
using UnityEngine;

//발판으로서 필요한 동작을 담은 스크립트
public class Platform : MonoBehaviour
{
    public GameObject[] obstacles;  //장애물 오브젝트 들
    private bool stepped = false;   //플레이어 캐릭터가 밟았는가 체크


    //컴포넌트가 활성화 될 때마다 매번 실행되는 메서드
    private void OnEnable()
    {
        //밟힘 상태를 리셋
        stepped = false;            //초기화 시켜줌

        for(int i = 0; i < obstacles.Length; i++)
        {
            //현재 순번의 장애물을 1/3 확률로 활성화
            if(Random.Range(0,3) == 0)   //0, 1, 2
            {
                obstacles[i].SetActive(true);       //0이 나오면 오브젝트 활성화
            }
            else
            {
                obstacles[i].SetActive(false);      //0이 아닐경우 비활성화
            }
        }
    }


    private void OnCollisionEnter2D(Collision2D collision)
    {
        //플레이어 캐리터가 자신을 밟았을 때 점수를 추가하는 처리
        //충돌한 상대방의 태그가 Player이고 이전에 플레이어 캘기터가 밟지 않았다면
        if(collision.collider.tag == "Player" && !stepped)
        {
            //점수를 추가하고, 밟힘 상태를 참으로 변경
            stepped = true;
            GameManager.Instance.AddScore(1);
        }
    }
    
}
